# Логические операторы в Python:

# < меньше - проверяет значение слева меньше значения справа?
# > больше ли значение слева, чем справа?
# == Равны ли значения слева и справа?
# != Отличаются ли значения слева и справа?
# <= Возвращает True, если значение слева меньше либо равно значению справа. Иначе — False
# >= Возвращает True, если значение слева больше либо равно значению справа. Иначе — False

